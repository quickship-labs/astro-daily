export function getCrystalById(id: string): Crystal | undefined {
  return crystals.find(crystal => crystal.id === id);
}

export function getCrystalsByZodiac(sign: string): Crystal[] {
  const normalizedSign = sign.toLowerCase();
  return crystals.filter(
    crystal =>
      crystal.zodiacSigns.includes(normalizedSign) ||
      crystal.zodiacSigns.includes('all')
  );
}

export function getCrystalsByChakra(chakra: string): Crystal[] {
  return crystals.filter(crystal =>
    crystal.chakra.toLowerCase().includes(chakra.toLowerCase())
  );
}

export function getCrystalsByProperty(property: string): Crystal[] {
  const normalizedProperty = property.toLowerCase();
  return crystals.filter(crystal =>
    crystal.properties.some(prop => prop.toLowerCase().includes(normalizedProperty))
  );
}
